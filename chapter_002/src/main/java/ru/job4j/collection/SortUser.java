        import java.util.Collection;
        import java.util.List;
        import java.util.Set;
        import java.util.TreeSet;
public class SortUser {

    public Set<User> sort(List<User> user) {
        Set<User> users = new TreeSet<>();
        for (User use : users){
            users.add(use);
        }
        return users;
    }
}
